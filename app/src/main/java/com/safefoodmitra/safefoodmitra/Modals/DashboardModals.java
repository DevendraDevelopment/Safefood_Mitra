package com.safefoodmitra.safefoodmitra.Modals;

public class DashboardModals {

    String txt;
    int img;

    public DashboardModals(String txt, int img) {
        this.txt = txt;
        this.img = img;
    }

    public String getTxt() {
        return txt;
    }

    public void setTxt(String txt) {
        this.txt = txt;
    }

    public int getImg() {
        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }
}
